    private void purchaseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_purchaseButtonActionPerformed
        // TODO add your handling code here:
        itemID = (int)idSpinner.getValue();
        numPurchases = (int)quantitySpinner.getValue();
        
        if(itemID == 1) {
        currentCost = 9.99;    
        } else if(itemID == 2) {
        currentCost = 15.99;   
        } else if(itemID == 3) {
        currentCost = 12.99;   
        } else if(itemID == 4) {
        currentCost = 13.5;   
        } else if(itemID == 5) {
        currentCost = 7;   
        } else if(itemID == 6) {
        currentCost = 19;   
        } else if(itemID == 7) {
        currentCost = 8.99;   
        } else if(itemID == 8) {
        currentCost = 6.99;   
        } else if(itemID == 9) {
        currentCost = 7.99;   
        } else if(itemID == 10) {
        currentCost = 3.99;   
        } else if(itemID == 11) {
        currentCost = 12.99;   
        } else if(itemID == 12) {
        currentCost = 7.99;   
        } else{
         currentCost = 0;   
        }
        
        totalCost = totalCost + currentCost * numPurchases;
        costLabel.setText("Total Cost: " + totalCost);
    }//GEN-LAST:event_purchaseButtonActionPerformed

